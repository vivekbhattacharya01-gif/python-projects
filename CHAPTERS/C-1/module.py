# COMMENTS IN PYTHON:

# In Python, comments are created using the hash symbol (#). Anything following the # on a line is considered a comment and is ignored by the Python interpreter.
# Comments are used to explain code, make it more readable, and provide context for other developers (or yourself) when you revisit the code later.

# There are two types of comments in Python:  
# 1. Single-line Comments: These comments occupy a single line and start with the # symbol.
# Example:
# This is a single-line comment

# 2. Multi-line Comments: These comments span multiple lines and are created using triple quotes (''' or """).
# Example:
# ''' HEYYYYYY.....''' / """ YOOOOO...."""


