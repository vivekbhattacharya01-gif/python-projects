                        # RULES FOR NAMING VARIABLES IN PYTHON---

    # ----------------------------------------------------------
#        1. Variable names can only contain letters (a-z, A-Z), digits (0-9), and underscores (_). 
#           They cannot contain spaces or special characters.
    # ---------------------------------------------------------
#        2. Variable names must start with a letter (a-z, A-Z) or an underscore (_).
#           They cannot start with a digit (0-9).
    # ----------------------------------------------------------
#        3. Variable names are case-sensitive, which means that uppercase and lowercase letters are treated as different characters.
#           For example, "myVariable" and "myvariable" are considered different variables.
    #  ----------------------------------------------------------
#        4. Variable names cannot be the same as Python keywords (reserved words) such as "if", "else", "while", "for", etc. 
#           These keywords have special meanings in Python and cannot be used as variable names.      
    #  -----------------------------------------------------------